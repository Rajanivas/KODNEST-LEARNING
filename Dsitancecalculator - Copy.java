package programs;

public class Dsitancecalculator {
	public double Distancecalculator(Double speed, Double time) {
		return speed * time;
		
	}
	
	public double Distancecalculator1(Double speed, Double time) {
		return speed / time;
		
	}

	public double Distancecalculator2(Double speed, Double time) {
		return speed - time;
		
	}


}
